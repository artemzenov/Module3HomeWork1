def count_calls():
    global calls
    calls += 1


def string_info(string):
    count_calls()
    return len(string), string.upper(), string.lower()


def is_contains(string, list_to_search):
    count_calls()

    for index_elem in range(len(list_to_search)):
        list_to_search[index_elem] = list_to_search[index_elem].lower()

    return string.lower() in list_to_search


calls = 0

